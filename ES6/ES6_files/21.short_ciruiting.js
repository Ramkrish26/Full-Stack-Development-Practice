// Logical operator || and && can be used for all data types

// Logical OR will return the first trtuthy value
console.log(2 || null) // 2
console.log(0 || 2) // 2
console.log(0 || null || 'Ram') // Ram

// Logical AND will return the first falsy value
console.log(2 && null) // null
console.log(0 && 2) // 0
console.log(0 && null && 'Ram') // 0
